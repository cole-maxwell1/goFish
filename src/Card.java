/**
 * An enumeration of the types of cards that can exist in the game
 * of Go Fish.
 *
 * NOTE: We do not need to include the suit of the card
 * because Go Fish does not evaluate the precedence of suits
 */
public enum Card
{
    ACE, TWO, THREE, FOUR, FIVE, SIX, SEVEN, EIGHT, NINE, TEN, JACK, QUEEN, KING
} // end Card